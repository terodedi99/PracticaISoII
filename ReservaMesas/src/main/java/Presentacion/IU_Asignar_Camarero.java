    private void cargarTabla() {
        String nombreColumnas [] = {"ID_CAMARERO", "DNI", "NOMBRE", "APELLIDOS", "TELEFONO"};
        
        for (String nombreColumna : nombreColumnas) {
            modelo.addColumn(nombreColumna);
        }
        
        TableColumn id = tableCamareros.getColumn("ID_CAMARERO");
        id.setMinWidth(80);
        
        TableColumn dni = tableCamareros.getColumn("DNI");
        dni.setMinWidth(80);
        
        TableColumn nombre = tableCamareros.getColumn("NOMBRE");
        nombre.setMinWidth(80);
        
        TableColumn apellidos = tableCamareros.getColumn("APELLIDOS");
        apellidos.setMinWidth(80);
        
        TableColumn tfno = tableCamareros.getColumn("TELEFONO");
        tfno.setMinWidth(80);
    }
    
    public void cargarDatos() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
        
        ArrayList<Empleado> lista = GestorCamarero.buscarListaCamareros(IU_Gestion_Reservas.sesionEmpleado.getnRestaurante());
        
        Object[] obj = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            Empleado e = lista.get(i);
            
            obj[0] = String.valueOf(e.getIdEmpleado());
            obj[1] = e.getDNI();
            obj[2] = e.getNombre();
            obj[3] = e.getApellidos();
            obj[4] = e.getTfno_contacto();
            
            modelo.addRow(obj);
        }
    }
    
    public void cargarServicio() {
        Servicio s = GestorServicio.buscarDatosServicio(Integer.parseInt(txtIdServicio.getText()));
        txtComentarios.setText(s.getComentarios());
        if (s.getNum_comensales() == 0) {
            cboNumComensales.setSelectedIndex(0);
        } else {
            cboNumComensales.setSelectedItem(String.valueOf(s.getNum_comensales()));
        }
    }

    private void cerrar() {
        this.dispose();
    }
    
