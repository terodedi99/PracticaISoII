    private void btnAsignarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAsignarActionPerformed
        int filaSeleccionada = tableCamareros.getSelectedRow();
        if (filaSeleccionada >= 0) {
            boolean exito = GestorServicio.asignarCamareroAServicio(Integer.parseInt(txtIdServicio.getText()), cboNumComensales.getSelectedItem().toString(), txtComentarios.getText(), Integer.parseInt(tableCamareros.getValueAt(filaSeleccionada, 0).toString()));
            if (exito) {
                this.setVisible(false);
                formGestion.setVisible(true);
                formGestion.cargarDatos();
                cerrar();
            } else {
                JOptionPane.showMessageDialog(null, "SE HA PRODUCIDO UN ERROR AL ASIGNAR EL CAMARERO", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "DEBES SELECCIONAR UN CAMARERO", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnAsignarActionPerformed

