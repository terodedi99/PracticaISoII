    private void cargarTabla() {
        String nombreColumnas [] = {"NOMBRE", "TIEMPO_TOTAL"};
        
        for (String nombreColumna : nombreColumnas) {
            modelo.addColumn(nombreColumna);
        }
        
        TableColumn id = tablaEstadisticas.getColumn("NOMBRE");
        id.setMinWidth(80);
        
        TableColumn fecha = tablaEstadisticas.getColumn("TIEMPO_TOTAL");
        fecha.setMinWidth(80);
    }
    
    public void cargarDatos() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
        
        ArrayList<Estadistica> lista = GestorEstadisticas.buscarEstadisticas(cboTipoTiempo.getSelectedIndex() + " " + cboTipoBusqueda.getSelectedIndex());
        
        Object[] obj = new Object[2];
        for (int i = 0; i < lista.size(); i++) {
            Estadistica e = lista.get(i);
            
            obj[0] = e.getNombreId();
            obj[1] = e.getTiempoTotal();
            
            modelo.addRow(obj);
        }
    }
    
