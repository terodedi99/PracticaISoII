    private void cargarTabla() {
        String nombreColumnas [] = {"ID_INGREDIENTE", "DESCRIPCION", "CANTIDAD ELABORACION", "UNIDAD", "CANTIDAD A COMPRAR"};
        
        for (String nombreColumna : nombreColumnas) {
            modelo.addColumn(nombreColumna);
        }
        
        TableColumn id = tablaIngredientes.getColumn("ID_INGREDIENTE");
        id.setMinWidth(80);
        
        TableColumn descripcion = tablaIngredientes.getColumn("DESCRIPCION");
        descripcion.setMinWidth(80);
        
        TableColumn elaboracion = tablaIngredientes.getColumn("CANTIDAD ELABORACION");
        elaboracion.setMinWidth(80);
        
        TableColumn unidad = tablaIngredientes.getColumn("UNIDAD");
        unidad.setMinWidth(80);
        
        TableColumn compra = tablaIngredientes.getColumn("CANTIDAD A COMPRAR");
        compra.setMinWidth(80);
    }
    
    public void cargarDatos() {
        modelo.getDataVector().removeAllElements();
        modelo.fireTableDataChanged();
        
        ArrayList<Elaboracion> lista = GestorAlmacen.readElaboracionesProducto(Integer.parseInt(this.txtId.getText()));
        
        Object[] obj = new Object[5];
        for (int i = 0; i < lista.size(); i++) {
            Elaboracion e = lista.get(i);
            
            obj[0] = e.getIngrediente().getIdIngrediente();
            obj[1] = e.getIngrediente().getDescripcionIngrediente();
            obj[2] = e.getCantidadElaboracion();
            obj[3] = e.getIngrediente().getUnidad();
            obj[4] = String.valueOf(0);
            
            modelo.addRow(obj);
        }
    }
    
